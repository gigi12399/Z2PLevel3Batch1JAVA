package Day18.bookmanagenmentsystem;

public class Author {
	private int id;
	private String name;
	private String city;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getCountry() {
		return city;
	}
	public void setCountry(String city) {
		this.city = city;
	}
	
}
